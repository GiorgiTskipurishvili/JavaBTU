public class Human {
    private String name;
    private int age;

    public Human(){
        System.out.println("Hello World");
    }

    public int getAge(){
        return age;
    }
    public void setAge(int age){
        this.age = age;
    }

    public String getName(){
        return name;
    }
    public void setName(String name){
        this.name = name;
    }

}
